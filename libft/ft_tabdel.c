#include "libft.h"

void	ft_tabdel(int **tab, int lines)
{
	int		i;

	i = 0;
	while (i < lines)
	{
		ft_memdel((void **)&(tab[i]));
		i++;
	}
	ft_memdel((void **)&(tab));
}
