#include "wolf3d.h"

void	look_up_down(t_map *map)
{
	if (map->look_up == 1)
		map->var += 5;
	if (map->look_down == 1)
		map->var -= 5;
	map->h2 += map->var;
	if (map->h2 > 960)
		map->h2 = 960;
	if (map->h2 < 0)
		map->h2 = 0;
}
