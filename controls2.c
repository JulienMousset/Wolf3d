#include "wolf3d.h"

void	look_up_down(int key, t_map *map)
{
	if (key == ARROW_UP)
		map->h2 += 20;
	if (key == ARROW_DOWN)
		map->h2 -= 20;
	if (map->h2 > 950)
		map->h2 = 950;
	if (map->h2 < 0)
		map->h2 = 0;
}
